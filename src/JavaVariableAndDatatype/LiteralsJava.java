package JavaVariableAndDatatype;

public class LiteralsJava {
    public static void main(String[] args) {

        byte age = 110;    // byte literals
        short num = 1500;  // short literals
        int num1 = 1500000000;  // integer literals
        long DinoAge = 160000000000L;  // long literals
        char alphabet = 'A';  // character literals
        float f1 = 5.6f;  // float literals
        double d1 = 16.6666;  // double literals
        boolean b1 = true;  // boolean literals
        System.out.println("byte : "+age);
        System.out.println("short : "+num);
        System.out.println("int : "+num1);
        System.out.println("long : "+DinoAge);
        System.out.println("char : "+alphabet);
        System.out.println("float : "+f1);
        System.out.println("double : "+d1);
        System.out.println("boolean : "+b1);

        // Non-Primitive Data Type Example for literals
        String str = "Krishna Pal";  // String literals
        System.out.println("String : "+str);
    }
}
