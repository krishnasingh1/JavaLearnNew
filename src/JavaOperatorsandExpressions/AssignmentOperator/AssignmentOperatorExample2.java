package JavaOperatorsandExpressions.AssignmentOperator;

public class AssignmentOperatorExample2 {
    public static void main(String[] args) {

        int a = 10;

        // +=, PlusEqualTo
        a += 3; // 10 + 3 = 13
        System.out.println(a);

        // -=, MinusEqualTo
        a -= 4; // 13 - 4 = 9
        System.out.println(a);

        // *=, MultiplyEqualTo
        a *= 2; // 9 * 2 = 18
        System.out.println(a);

        // /=, DivideEqualTo
        a /= 2; // 18 / 2 = 9
        System.out.println(a);

        // %=, ModEqualTo
        a %= 2; // 9 % 2 = 1
        System.out.println(a);
    }
}
