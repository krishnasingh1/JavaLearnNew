package JavaOperatorsandExpressions.ArithmaticOperator;

public class OperatorExample {
    public static void main(String[] args) {

        // initializing variables
        int a = 10;
        int b = 5;

        // Operations
        // additions
        System.out.println(a+b);
        // subtraction
        System.out.println(a-b);
        // multiplication
        System.out.println(a*b);
        // division
        System.out.println(a/b);
        // modulus
        System.out.println(a%b);

        // operator Example
        System.out.println(10*10/5+3-1*4/2);
    }
}
